public class Ponto {
    //variaveis
    private double x;
    private double y;

    //constutor personalizado


    public Ponto(double x, double y) {
        this.x = x;
        this.y = y;
    }

    //metodos get set


    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    //
}

