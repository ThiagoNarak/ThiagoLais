package Exercicios.AtivPraticaIV.ThiagoPratica2;


public class Ponto {

    //------------=ATRIBUTOS
    private double x,y;


    //------------=CONSTRUTOR
        public Ponto(double x, double y) {
        this.x = x;
        this.y = y;
    }
    //------------=GETSETS

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
